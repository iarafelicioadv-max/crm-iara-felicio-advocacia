const TEMPLATE_POR_ID = Object.fromEntries(CHECKLIST_TEMPLATES.map((t) => [t.id, t]));

const ITEM_POR_CODIGO = {};
for (const t of CHECKLIST_TEMPLATES) {
  for (const item of t.itens) {
    ITEM_POR_CODIGO[item.codigo] = { ...item, templateId: t.id };
  }
}

const CODIGOS_VALIDOS = new Set(Object.keys(ITEM_POR_CODIGO));

